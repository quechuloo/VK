package domain;

public class Repost {

    private int count;
    private int userReposted;

    public void setCount(int count) {
        this.count = count;
    }

    public int getCount() {
        return count;
    }

    public void setUserReposted(int userReposted) {
        this.userReposted = userReposted;
    }

    public int getUserReposted() {
        return userReposted;
    }

}
